// =====================  DEFINITION D'UNE CLASSE ABSTRAITE  =====================


//      ==> On définit une classe abstraite avec le modificateur "abstract" AVANT le mot clé "class"

//      ==> Une classe abstraite ne peut pas être INSTANCIEE !!!!!!

//      ==> Une classe abstraite peut_être "SOUS-CLASSEE" dans une autre classe, mais elle ne peut pas être INSTANCIEE


//      ==> Une classe abstraite peut déclarer des méthodes ABSTRAITES et CONCRETES

//      =====> ATTENTION : Si CLASSE ABSTRAITE CONTIENT DES METHODE(S) ABSTRAITE = signature
//
//              - Si une classe définit une ou plusieurs méthodes abstract :

//                      -->   alors la classe elle-même DOIT être DECLAREE abstract


//              - Une sous-classe dérivée d'une classe abstraite doit soit implémenter

//                      -->   toutes les méthodes abstraites de la classe de base, soit être abstraite elle-même
